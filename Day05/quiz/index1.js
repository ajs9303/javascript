// time을 AM,PM을 없애고 24시화 하기 ex) 4:48PM => 16:48
// timeZone을 대륙만 남기기로 하기
const q1 = data.map((v) => {
  v.fullName = v.fullName.split(" ")[1].toLowerCase();
  const amPm = v.time.split(" "); // 시간과 AMPM 나누기
  const hM = amPm[0].split(":"); // 시 분 나누기

  v.time = amPm[1] == "AM" ? amPm[0] : +hM[0] + 12 + ":" + hM[1];
  v.timeZone = v.timeZone.split("/")[0];
  return v;
});
